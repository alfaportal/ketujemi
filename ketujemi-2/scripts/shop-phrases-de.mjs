/** sq → de direct (from Albanian taxonomy names). */
export const SHOP_SQ_DE = Object.fromEntries(
  SHOP_PHRASES_EN.map(([sq, en]) => {
    const de = EN_DE[en] ?? EN_DE[titleCaseEn(en)];
    return [sq, de ?? en];
  }),
);

export function shopEnglishToGerman(en) {
  if (!en) return en;
  if (EN_DE[en]) return EN_DE[en];
  const tc = titleCaseEn(en);
  if (EN_DE[tc]) return EN_DE[tc];
  return en;
}
