function normalize(s) {
  return s
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/ë/g, "e")
    .replace(/ç/g, "c");
}

function hash(s) {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  return h;
}

function poolFor(categorySlug) {
  const base = PHOTO_POOLS[categorySlug] ?? [];
  const hero = SHOP_DIRECTORY_CATEGORY_IMAGE_URLS[categorySlug];
  if (hero && !base.includes(hero.replace("w=400", "w=200"))) {
    return [hero.replace("w=400", "w=200"), ...base];
  }
  return base.length ? base : [hero?.replace("w=400", "w=200") ?? u("1492144534655-ae79c964c9d7")];
}

function photoForSub(categorySlug, subSlug, nameSq) {
  const norm = normalize(nameSq);
  for (const [kw, url] of KEYWORD_RULES) {
    if (norm.includes(normalize(kw))) return url;
  }
  const pool = poolFor(categorySlug);
  return pool[hash(`${categorySlug}/${subSlug}`) % pool.length];
}

