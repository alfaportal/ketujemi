/** sq → fr direct (from Albanian taxonomy names). */
export const SHOP_SQ_FR = Object.fromEntries(
  SHOP_PHRASES_EN.map(([sq, en]) => {
    const fr = EN_FR[en] ?? EN_FR[titleCaseEn(en)];
    return [sq, fr ?? en];
  }),
);

export function shopEnglishToFrench(en) {
  if (!en) return en;
  if (EN_FR[en]) return EN_FR[en];
  const tc = titleCaseEn(en);
  if (EN_FR[tc]) return EN_FR[tc];
  return en;
}
